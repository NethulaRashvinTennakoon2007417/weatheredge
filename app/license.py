"""
Weather Edge Analyzer — License System
=======================================
Access codes are generated by you (the seller) and given to paying subscribers.
Each code encodes: expiry date + unique subscriber ID.
No internet needed to validate — pure local crypto.

HOW IT WORKS:
  1. You run: python generate_code.py --days 30 --id SUB001
  2. You send the code to the buyer via DM
  3. Buyer enters it once on first launch — stored locally
  4. Every launch checks: code valid? not expired? show the app
  5. Each month you send a new code after they pay

TAMPER PROOF:
  Codes are HMAC-SHA256 signed with your secret key.
  Changing the expiry date in the stored file invalidates it.
  The secret key never leaves your machine.
"""

